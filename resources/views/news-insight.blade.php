                    {{-- Lead Paragraph --}}
                    <p
                        class="text-xl md:text-2xl text-gray-900 font-medium leading-relaxed border-b border-gray-200 pb-10 mb-10">
                        On 31 October 2025, the White House said Nigeria would be redesignated a <strong>Country of
                            Particular Concern (CPC)</strong> under America’s International Religious Freedom Act,
                        opening the door to targeted sanctions and aid restrictions.
                    </p>

                    <p>
                        The announcement arrived alongside combative rhetoric about the “killing of Christians” and even
                        talk of possible U.S. military action. Abuja pushed back immediately, calling the charge
                        misleading and insisting violence is complex and not driven solely by religion.
                    </p>

                    <h2 class="text-3xl font-bold text-gray-900 mt-12 mb-6">Is it genocide?</h2>
                    <p>
                        The UN Genocide Convention standard turns on specific intent to destroy a group “in whole or in
                        part.” Though Nigeria’s violence is horrendous—spanning Boko Haram/ISWAP atrocities, rural
                        banditry, farmer–herder conflicts, and communal reprisals—the national pattern does not
                        demonstrate state policy or a systematic campaign to exterminate Christians as a group.
                    </p>
                    <p>
                        Attacks frequently target whoever is accessible: villagers on market day, travellers on
                        highways, entire communities locked in land-use feuds, and clerics of both faiths. That is why
                        even when Washington tightens pressure, its own advisory machinery tends to speak of
                        “particularly severe violations of religious freedom,” not genocide.
                    </p>

                    <h3 class="text-xl font-bold text-gray-900 mt-8 mb-3">What the data suggests</h3>
                    <p>
                        Faith-based monitors (e.g., Open Doors) and Nigerian civil-society groups have published tallies
                        indicating very high Christian casualties since 2009; others stress heavy Muslim death tolls
                        across the northeast and northwest. Counting is messy; motives are mixed; labels are contested.
                        The common thread is a failure of protection and justice, not a single ideological campaign of
                        extermination.
                    </p>

                    <h2 class="text-3xl font-bold text-gray-900 mt-12 mb-6">Why is the U.S. moving now?</h2>
                    <p>There are three overlapping drivers likely responsible for this shift:</p>
                    <ul class="list-decimal pl-6 space-y-4 marker:text-gray-900 marker:font-bold mt-4">
                        <li>
                            <strong>Domestic politics & constituency signaling:</strong> U.S. legislators and advocacy
                            organisations on the Christian right have campaigned hard on Nigeria, pressing the
                            administration to restore CPC and threatening new legislation to mandate sanctions.
                        </li>
                        <li>
                            <strong>Bureaucratic continuity:</strong> USCIRF’s 2025 report again recommended CPC for
                            Nigeria, citing impunity, attacks by jihadist groups, and blasphemy cases.
                        </li>
                        <li>
                            <strong>Strategic leverage after Niger:</strong> With U.S. force posture unsettled in the
                            Sahel, Abuja’s cooperation on counter-insurgency matters more. A CPC label threatens visa
                            bans and aid conditions to extract policy concessions without deploying troops.
                        </li>
                    </ul>

                    {{-- UPDATED SECTION: Who Gains, Who Loses (Normal Text) --}}
                    <h2 class="text-3xl font-bold text-gray-900 mt-12 mb-6">Who gains, who loses</h2>

                    <p>
                        <strong>Winners in Washington:</strong> Lawmakers courting evangelical voters; advocacy groups
                        that can point to a policy “win”; agencies seeking sticks, not just carrots, in Sahel policy.
                    </p>

                    <p>
                        <strong>Losers in Abuja:</strong> Foreign-ministry diplomats who must now fend off sanctions
                        talk and defend governors or police commanders named in Hill drafts. Nigerian industry could
                        face de-risking by U.S. partners if the CPC label is bundled with human-rights due-diligence
                        clauses.
                    </p>

                    <p>
                        <strong>Civilians in the line of fire:</strong> The designation changes little on the ground
                        unless it catalyses better policing, rapid prosecutions, and victim support. There is no
                        evidence that loose “genocide” talk, by itself, saves lives.
                    </p>

                    <h2 class="text-3xl font-bold text-gray-900 mt-12 mb-6">What Could Truly Make a Difference</h2>

                    <h4 class="font-bold text-gray-900 text-xl mt-6">For Abuja</h4>
                    <ul class="list-disc pl-6 space-y-3 mb-6 mt-2">
                        <li><strong>Take ownership of the data:</strong> Regularly publish detailed dashboards tracking
                            all victims regardless of faith.</li>
                        <li><strong>End impunity:</strong> Establish special courts for violent crimes in the
                            hardest-hit regions.</li>
                        <li><strong>Reform blasphemy laws:</strong> Issue federal guidance to help state courts avoid
                            religious-freedom violations.</li>
                    </ul>

                    <h4 class="font-bold text-gray-900 text-xl mt-8">For Washington</h4>
                    <ul class="list-disc pl-6 space-y-3 mt-2">
                        <li><strong>Sanction with precision:</strong> Focus on those responsible for mass violence,
                            avoiding harm to broader humanitarian aid.</li>
                        <li><strong>Invest in real protection:</strong> Prioritize funding for grassroots early-warning
                            systems and rural security.</li>
                        <li><strong>Use words wisely:</strong> Only label events as “genocide” when the evidence meets
                            the strict threshold.</li>
                    </ul>

                    <div class="bg-blue-50 border-l-4 border-blue-600 p-8 mt-12 rounded-r-lg">
                        <h3 class="text-blue-900 font-bold mb-2 uppercase text-sm tracking-wide">Bottom Line</h3>
                        <p class="text-blue-900 text-lg leading-relaxed">
                            Nigeria’s crisis is brutal and intolerable. Christians have been killed in large numbers, so
                            have Muslims. The pattern looks like state weakness and criminal predation, not a
                            government-led campaign to destroy Christians. Treat the CPC label as a warning shot on
                            impunity, not as proof of genocide.
                        </p>
                    </div>

